const angka = [1, 2, 3, 4, 5];

// Dari terkecil ke terbesar
for (let i = 0; i < angka.length; i++) {
    console.log(angka[i]);
}

// Dari terbesar ke terkecil
for (let i = 5; i >= 0; i--) {
    console.log(i);
}

// Perulangan jumlah tertentu
for (let i = 0; i < 8; i++) {
    console.log("Pengulangan ke-" + (i + 1));
}


